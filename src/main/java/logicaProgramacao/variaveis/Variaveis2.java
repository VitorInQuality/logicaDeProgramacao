package logicaProgramacao.variaveis;

public class Variaveis2 {

	public static void main(String[] args) {

		int numeroInteiro;
		double numeroQuebrado;
		boolean verdadeiroFalso;

		numeroInteiro = 10;
		System.out.println("O número inteiro é  " + numeroInteiro);

		numeroQuebrado = 20.0;
		System.out.println("O número quebrado é " + numeroQuebrado);

		verdadeiroFalso = true;
		System.out.println("O número é " + verdadeiroFalso);
		
		verdadeiroFalso = false;
		System.out.println("O número é " + verdadeiroFalso);
		 

	}
}
